namespace TravianBot
{
    /// <summary>
    ///     Tribe types.
    /// </summary>
    public enum Tribe
    {
        /// <summary>
        ///     Default tribe value. If player's tribe is not
        ///     known then this value will be used.
        /// </summary>
        UNKNOWN,

        /// <summary>
        ///     Gaul tribe.
        /// </summary>
        GAUL,

        /// <summary>
        ///     Roman tribe.
        /// </summary>
        ROMAN,

        /// <summary>
        ///     Teuton tribe.
        /// </summary>
        TEUTON,

        /// <summary>
        ///     Natar tribe.
        /// </summary>
        NATAR,

        /// <summary>
        ///     Nature tribe. No one owns.
        /// </summary>
        NATURE
    }
}